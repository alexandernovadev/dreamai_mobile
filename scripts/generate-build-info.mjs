/**
 * Ejecutar antes de builds de producciĂłn (`npm run build-info`) o en `postinstall`.
 * Escribe `lib/buildInfo.generated.ts` con versiĂłn, fecha ISO y commit git opcional.
 * `EXPO_PUBLIC_APP_ENV` se inyecta en tiempo de generaciĂłn (p. ej. en CI antes de EAS).
 */
import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const iso = new Date().toISOString();

let gitCommit = '';
try {
  gitCommit = execSync('git rev-parse --short HEAD', {
    cwd: root,
    encoding: 'utf8',
  }).trim();
} catch {
  // Sin repo git
}

const appEnv =
  process.env.EXPO_PUBLIC_APP_ENV?.trim() ||
  process.env.APP_ENV?.trim() ||
  'development';

const out = join(root, 'lib', 'buildInfo.generated.ts');
writeFileSync(
  out,
  `// Auto-generated by scripts/generate-build-info.mjs â€” do not edit.
export const APP_BUILD_ISO_DATE = '${iso}';
export const APP_GIT_COMMIT = ${JSON.stringify(gitCommit)};
export const APP_PACKAGE_VERSION = ${JSON.stringify(pkg.version ?? '0.0.0')};
export const APP_ENV_LABEL = ${JSON.stringify(appEnv)};
`,
  'utf8',
);
